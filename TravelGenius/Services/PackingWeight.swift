import Foundation

enum PackingWeight {
    /// 單件估計重量（公克）
    static func grams(for item: PackingItem) -> Int {
        let name = item.name
        for (keywords, grams) in keywordMap where keywords.contains(where: name.contains) {
            return grams
        }
        return categoryFallback[item.category] ?? 200
    }

    /// 顯示用格式：≥1kg 以 kg（一位小數），否則以 g。
    static func format(grams: Int) -> String {
        grams >= 1000 ? String(format: "%.1f kg", Double(grams) / 1000.0) : "\(grams) g"
    }

    // 順序：具體／重的在前，通用在後（避免「衣」先吃掉「外套」）
    private static let keywordMap: [([String], Int)] = [
        (["筆電", "電腦"], 1500),
        (["平板"], 500),
        (["相機"], 500),
        (["鞋"], 700),
        (["外套", "大衣", "羽絨"], 600),
        (["雨衣"], 300),
        (["水瓶", "水壺", "保溫瓶"], 300),
        (["書", "雜誌"], 300),
        (["傘", "雨具"], 350),
        (["褲"], 350),
        (["行動電源", "充電寶"], 250),
        (["手機"], 200),
        (["泳"], 200),
        (["拖鞋"], 200),
        (["零食", "餅乾", "糖", "點心"], 150),
        (["盥洗", "洗髮", "沐浴", "洗面", "毛巾", "肥皂", "牙刷"], 150),
        (["防曬", "保養", "乳液", "化妝", "面膜", "保濕"], 120),
        (["現金", "錢包", "皮夾"], 100),
        (["轉接", "插座", "變壓"], 100),
        (["刮鬍"], 100),
        (["帽"], 100),
        (["內衣", "貼身", "內著"], 80),
        (["充電頭"], 80),
        (["體溫", "溫度計"], 80),
        (["面紙", "衛生紙", "濕紙巾", "紙巾", "方巾"], 80),
        (["耳機"], 60),
        (["充電", "傳輸線", "線材", "線"], 60),
        (["藥", "止痛", "腸胃", "感冒", "暈車"], 60),
        (["OK繃", "創可貼", "繃帶", "急救"], 50),
        (["電池"], 50),
        (["鑰匙"], 50),
        (["牙"], 50),
        (["襪"], 40),
        (["護照", "簽證"], 30),
        (["太陽眼鏡", "墨鏡", "眼鏡", "隱形"], 30),
        (["口罩"], 30),
        (["筆"], 15),
        (["信用卡", "金融卡", "卡片", "票", "影本", "保單", "聯絡卡"], 15),
        (["衣", "上衣", "服"], 200),
    ]

    private static let categoryFallback: [PackingCategory: Int] = [
        .clothing: 200,
        .electronics: 300,
        .documents: 30,
        .toiletries: 150,
        .health: 60,
        .other: 200,
    ]
}
